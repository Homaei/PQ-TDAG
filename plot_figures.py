        ax.axhline(lim,linestyle=":",linewidth=0.8,color="gray",alpha=0.6)
        ax.text(NR[-1]*0.97,lim+8,lbl,ha="right",va="bottom",
                fontsize=STYLE["annot_size"],color="gray")

