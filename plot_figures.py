def rc():
    cfg = {
        "font.family":  STYLE["font_family"],
        "font.size":    STYLE["font_size"],
        "axes.labelsize": STYLE["label_size"],
        "axes.titlesize": STYLE["title_size"],
        "legend.fontsize":STYLE["legend_size"],
        "xtick.labelsize":STYLE["tick_size"],
        "ytick.labelsize":STYLE["tick_size"],
        "figure.dpi":   STYLE["screen_dpi"],
        "axes.grid":    True,
        "grid.alpha":   STYLE["grid_alpha"],
        "grid.linestyle":STYLE["grid_ls"],
    }
    if STYLE["font_name"]:
        rc_key = "font.serif" if STYLE["font_family"] == "serif" else "font.sans-serif"
        cfg[rc_key] = [STYLE["font_name"]]
    plt.rcParams.update(cfg)


def co(sid):  return STYLE["colors"].get(sid, "#888888")
def mk(sid):  return STYLE["markers"].get(sid, "o")
def lw(sid):  return STYLE["lw_main"] if sid == "pq_tdag" else STYLE["lw_base"]
def ms(sid):  return STYLE["ms_main"] if sid == "pq_tdag" else STYLE["ms_base"]


def load_schemes() -> dict:
    p = DATA / "crypto_timings.json"
    if not p.exists():
        sys.exit(f"ERROR: {p} not found. Run step_2 and step_2b first.")
    with open(p) as f:
        return json.load(f)["schemes"]


def csv(name: str) -> pd.DataFrame | None:
    p = DATA / name
    return pd.read_csv(p) if p.exists() else None


def save(fig, name: str, extra: Path | None = None):
    for d in ([FIG] + ([extra] if extra else [])):
        for ext in ["pdf", "png"]:
            fig.savefig(d / f"{name}.{ext}",
                        bbox_inches="tight", dpi=STYLE["save_dpi"])
    plt.close(fig)
    print(f"  ✓  {name}")


def bw_naive(N, f, sig):  return N * f * (S_PL + sig) * 8 / 1e6
def bw_pq(N, f, sig, M):  return N * (f / M) * (M * S_PL + sig) * 8 / 1e6


