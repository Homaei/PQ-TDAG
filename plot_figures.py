def fig_E3(s):
    rc(); fig,ax = plt.subplots(figsize=(STYLE["col_width"],STYLE["height"]))
    TI = np.linspace(0,60,200)

    def stor(t,sig,M,pq):
        ts = t*60
        if pq:
            n_t = N_REF*(F_REF/M)*ts
            n_i = N_REF*F_REF*ts*(M-1)/M
            return (n_i*(S_PL+32)+n_t*(S_PL+sig+64))/(1024*1024)
        return N_REF*F_REF*ts*(S_PL+sig+64)/(1024*1024)

    for sid in ["pq_tdag","naive_mldsa44","falcon512","mldsa65","xmssmt","ecdsa"]:
        if sid not in s: continue
        sig = s[sid].get("sig_bytes",2420)
        M   = M_REF if sid=="pq_tdag" else 1
        label = "PQ-TDAG (Ours)" if sid=="pq_tdag" else s[sid].get("label",sid)
        ax.plot(TI,[stor(t,sig,M,sid=="pq_tdag") for t in TI],
                color=co(sid),linewidth=lw(sid),label=label)

    ax.set(xlabel="Simulation Time (min)",ylabel="Cumulative Storage (MB)",
           title=f"DAG Storage Growth ($N={N_REF}$)",xlim=(0,60))
    ax.legend(fontsize=STYLE["legend_size"]-0.5,framealpha=0.9)
