def fig_D2():
    import math
    rc(); fig,(ax1,ax2) = plt.subplots(1,2,
        figsize=(STYLE["full_width"], STYLE["height"]-0.5))
    PE   = np.linspace(0,0.25,80)
    GAMA = 1-1e-5
    TS,TP,TTIP,TRTX = 0.0436,1.0,0.5,3.0
    MVALS= [3,5,8]
    MC_  = [co("falcon512"),co("pq_tdag"),co("mldsa65")]

    for M,col in zip(MVALS,MC_):
        tbfr,arq,rate = [],[],[]
        for pe in PE:
            if pe<=0: r=0
            else:
                try:
                    v=math.log(1e-5/max(M-1,1))/math.log(pe)-1
                    r=max(0,math.ceil(v))
                except: r=0
            tbfr.append(TS+TTIP+M*TP+r*TRTX)
            arq.append(TS+TTIP+M*TP+(M-1)*(pe/max(1-pe,1e-9))*TRTX)
            rt = max(0, 1-(M-1)*(pe**(
                max(0,int((T_MAX-TS-TTIP-M*TP)/TRTX))+1)))
            rate.append(rt*100)
        ax1.plot(PE*100,tbfr,color=col,linewidth=STYLE["lw_base"],
                 label=f"TBFR $M={M}$")
        ax1.plot(PE*100,arq, color=col,linewidth=STYLE["lw_base"]-0.3,
                 linestyle="--",label=f"ARQ $M={M}$")
        ax2.plot(PE*100,rate,color=col,linewidth=STYLE["lw_base"],
                 label=f"$M={M}$")

    ax1.axhline(T_MAX,color=co("tmax"),linestyle="-.",
                linewidth=STYLE["lw_ref"],label=f"$T_{{\\max}}$")
    ax1.set(xlabel="$p_e$ (\\%)",ylabel="Latency (ms)",
            title="(a) Recovery Time",xlim=(0,25),ylim=(0,T_MAX*2.5))
    ax1.legend(fontsize=STYLE["legend_size"]-0.5,ncol=2)

    ax2.axhline(GAMA*100,color=co("tmax"),linestyle="-.",
                linewidth=STYLE["lw_ref"],label="$\\gamma_{\\mathrm{req}}$")
    ax2.set(xlabel="$p_e$ (\\%)",ylabel="Success Rate (\\%)",
            title="(b) TBFR Success",xlim=(0,25),ylim=(99.0,100.01))
    ax2.legend(fontsize=STYLE["legend_size"])
    plt.tight_layout(); save(fig,"fig_D2_tbfr_vs_arq")


